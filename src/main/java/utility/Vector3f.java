package utility;

/**
 * @author ReverendDread
 * Jul 10, 2018
 */
public class Vector3f {

	public float x;
	public float y;
	public float z;
	
	/**
	 * Creates a new {@code Vector3f}
	 * 
	 * @param x
	 * 			
	 * @param y
	 * 
	 * @param z
	 * 
	 */
	public Vector3f(final float x, final float y, final float z) {
		this.x = x;
		this.y = y;
		this.z = z;
	}
	
}
